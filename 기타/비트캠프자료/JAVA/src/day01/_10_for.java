package day01;

public class _10_for {

	public static void main(String[] args) {
		
	//1. for 반복문
		int sum = 0;
		
		//1~100까지 합을 구하기
		//초기화된 변수 i가 100보다 작거나 같을 때 계속해서 중괄호 안의 내용이 실행 됨
		for(int i = 0; i <= 100; i++) {
			sum += i;
		}
		
		System.out.println("1~100까지의 합은 : "+sum);
		
		
		
		
		
		
		

	}

}
