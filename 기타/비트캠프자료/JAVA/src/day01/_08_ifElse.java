package day01;

public class _08_ifElse {

	public static void main(String[] args) {
		
	//1. if elseif else 구문
		int score = 75;
		
		//score가 90점 이상이면 A등급
		if(score >= 90) {
			System.out.println("A등급입니다");
		//score가 80점 이상이면 B등급
		} else if(score >= 80) {
			System.out.println("B등급입니다");
		//score가 70점 이상이면 C등급
		} else if(score >= 70) {
			System.out.println("C등급입니다");
		//score가 70점 미만이면 D등급
		} else {
			System.out.println("D등급입니다");
		}
		
		
		
		
		
		
		
		

	}

}
